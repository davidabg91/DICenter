function updateGallery() {
    if (!currentGallery) return;
    const img = document.getElementById('gallery-img');
    const title = document.getElementById('gallery-title');
    const current = document.getElementById('gallery-current');
    const total = document.getElementById('gallery-total');

    img.src = currentGallery.images[currentGalleryIndex];
    title.textContent = currentGallery.title[currentLang];
    current.textContent = currentGalleryIndex + 1;
    total.textContent = currentGallery.images.length;
}

function changeGalleryImage(dir) {
    if (!currentGallery) return;
    currentGalleryIndex += dir;
    if (currentGalleryIndex >= currentGallery.images.length) currentGalleryIndex = 0;
    if (currentGalleryIndex < 0) currentGalleryIndex = currentGallery.images.length - 1;
    updateGallery();
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('di_center_lang', lang);
    document.documentElement.lang = lang;

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (TRANSLATIONS[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = TRANSLATIONS[lang][key];
            } else {
                // If it contains an icon (i element), keep it
                const icon = el.querySelector('i[data-lucide]');
                if (icon) {
                    el.innerHTML = TRANSLATIONS[lang][key] + ' ';
                    el.appendChild(icon);
                    lucide.createIcons(); // Re-init icons if injected via innerHTML
                } else {
                    el.innerText = TRANSLATIONS[lang][key];
                }
            }
        }
    });

    // Update Safe Images
    const safePreviewImg = document.getElementById('safe-preview-img');
    if (safePreviewImg) safePreviewImg.src = 'safeinstruc.png';

    // Handle month transition for calendar
    if (document.getElementById('calendar-grid')) {
        initAvailabilityCalendar(); // Re-render calendar with new names
    }
    
    // Update active class on switcher UI if implemented
    const switchers = document.querySelectorAll('.lang-btn');
    switchers.forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('onclick').includes(lang));
    });
}

