// --- Calendar Logic Update ---
async function initAvailabilityCalendar() {
    const calendarGrid = document.getElementById('calendar-grid');
    if (!calendarGrid) return;

    const icalUrl = 'https://bg.airbnb.com/calendar/ical/871070368525746121.ics?t=83b1cd1c4f6b4d6cbfdab8f8397000c7';
    const proxyUrl = 'https://corsproxy.io/?' + encodeURIComponent(icalUrl);

    try {
        const response = await fetch(proxyUrl);
        const icalData = await response.text();
        const reservedRanges = parseIcal(icalData);
        renderCalendar(reservedRanges);
    } catch (error) {
        calendarGrid.innerHTML = `<div class="calendar-loader">${TRANSLATIONS[currentLang]['calendar-loader']}</div>`;
    }
}

function parseIcal(data) {
    const events = [];
    const veins = data.split('BEGIN:VEVENT');
    veins.shift();
    veins.forEach(vein => {
        const startMatch = vein.match(/DTSTART;VALUE=DATE:(\d{8})/);
        const endMatch = vein.match(/DTEND;VALUE=DATE:(\d{8})/);
        if (startMatch && endMatch) {
            events.push({
                start: parseIcalDate(startMatch[1]),
                end: parseIcalDate(endMatch[1])
            });
        }
    });
    return events;
}

function parseIcalDate(str) {
    return new Date(str.substring(0, 4), str.substring(4, 6) - 1, str.substring(6, 8));
}

function renderCalendar(reservedRanges) {
    const container = document.getElementById('calendar-grid');
    container.innerHTML = '';
    const now = new Date();
    for (let i = 0; i < 2; i++) {
        container.appendChild(generateMonth(new Date(now.getFullYear(), now.getMonth() + i, 1).getMonth(), new Date(now.getFullYear(), now.getMonth() + i, 1).getFullYear(), reservedRanges));
    }
}

function generateMonth(month, year, reservedRanges) {
    const mNames = TRANSLATIONS[currentLang]['months'];
    const dNames = TRANSLATIONS[currentLang]['days'];
    
    const wrapper = document.createElement('div');
    wrapper.className = 'calendar-month';
    const title = document.createElement('h4');
    title.className = 'month-title';
    title.innerText = `${mNames[month]} ${year}`;
    wrapper.appendChild(title);
    
    const grid = document.createElement('div');
    grid.className = 'days-grid';
    dNames.forEach(name => {
        const dName = document.createElement('div');
        dName.className = 'day-name';
        dName.innerText = name;
        grid.appendChild(dName);
    });
    
    const firstDay = new Date(year, month, 1).getDay();
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    let offset = firstDay === 0 ? 6 : firstDay - 1;
    
    for (let i = 0; i < offset; i++) {
        const empty = document.createElement('div');
        empty.className = 'day empty';
        grid.appendChild(empty);
    }
    
    const today = new Date();
    today.setHours(0, 0, 0, 0);

    for (let d = 1; d <= daysInMonth; d++) {
        const current = new Date(year, month, d);
        const dayEl = document.createElement('div');
        dayEl.className = 'day';
        dayEl.innerText = d;
        const isToday = current.getTime() === today.getTime();
        const isReserved = reservedRanges.some(range => current >= range.start && current < range.end);
        if (isToday) dayEl.classList.add('today');
        if (isReserved) dayEl.classList.add('reserved');
        if (!isReserved) dayEl.classList.add('available');
        grid.appendChild(dayEl);
    }
    wrapper.appendChild(grid);
    return wrapper;
}
